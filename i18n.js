// 当前语言
var curLang = localStorage.getItem('lang') || 'zh-CN';
function t(key){ return (LANGS[curLang] && LANGS[curLang][key]) || (LANGS['en'] && LANGS['en'][key]) || key; }
function setLang(code){ curLang=code; localStorage.setItem('lang',code); applyLang(); }

function applyLang(){
  // RTL语言
  var rtl=['ar','ur'];
  document.body.dir = rtl.includes(curLang) ? 'rtl' : 'ltr';
  // 更新所有带data-i18n属性的元素
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    var key=el.getAttribute('data-i18n');
    if(el.tagName==='INPUT'||el.tagName==='TEXTAREA') el.placeholder=t(key);
    else el.textContent=t(key);
  });
}
